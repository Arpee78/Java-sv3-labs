package math.math;

public class MathMethods {

    public static void main(String[] args) {
        System.out.println(Math.max(2, 3));
        System.out.println(Math.min(2, 3));
        System.out.println(Math.PI);
        System.out.println(Math.round(Math.PI));
        System.out.println(Math.abs(-7));
        System.out.println(Math.negateExact(1));
        System.out.println(Math.addExact(1,3));
        System.out.println(Math.subtractExact(1,3));
        System.out.println(Math.multiplyExact(2,3));
        System.out.println(Math.incrementExact(3));
        System.out.println(Math.decrementExact(3));
        System.out.println(Math.random());
    }
}
